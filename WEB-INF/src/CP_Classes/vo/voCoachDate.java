package CP_Classes.vo;


public class voCoachDate {
	
	int PK=0;
	int FKCoachDateGroup=0;
	String date=new String();
	public voCoachDate() {
		super();
	}
	
	

	public voCoachDate(int pK, int fKCoachDateGroup, String date) {
		super();
		PK = pK;
		FKCoachDateGroup = fKCoachDateGroup;
		this.date = date;
	}



	public int getPK() {
		return PK;
	}
	public void setPK(int pK) {
		PK = pK;
	}
	
	public int getFKCoachDateGroup() {
		return FKCoachDateGroup;
	}



	public void setFKCoachDateGroup(int fKCoachDateGroup) {
		FKCoachDateGroup = fKCoachDateGroup;
	}



	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	

}
