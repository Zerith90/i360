package CP_Classes.vo;

import java.util.Date;

public class votblEvent {

	private int	iEventID = 0 ;
	private String	sEventAction	= "" ;
	private String sItem	= "" ;
	private String	sDescription = "" ;
	private String	sLoginName	= "" ;
	private String	sCompanyName	= "" ;
	private String	sOrganizationName	= "" ;
	private Date	dDeletedDate;
	
	public String getCompanyName() {
		return sCompanyName;
	}
	public void setCompanyName(String companyName) {
		sCompanyName = companyName;
	}
	
	public String getDescription() {
		return sDescription;
	}
	public void setDescription(String description) {
		sDescription = description;
	}
	public String getEventAction() {
		return sEventAction;
	}
	public void setEventAction(String eventAction) {
		sEventAction = eventAction;
	}
	public int getEventID() {
		return iEventID;
	}
	public void setEventID(int eventID) {
		iEventID = eventID;
	}
	public String getItem() {
		return sItem;
	}
	public void setItem(String item) {
		sItem = item;
	}
	public String getLoginName() {
		return sLoginName;
	}
	public void setLoginName(String loginName) {
		sLoginName = loginName;
	}
	public String getOrganizationName() {
		return sOrganizationName;
	}
	public void setOrganizationName(String organizationName) {
		sOrganizationName = organizationName;
	}
	public Date getDeletedDate() {
		return dDeletedDate;
	}
	public void setDeletedDate(Date deletedDate) {
		dDeletedDate = deletedDate;
	}
	
	
}
