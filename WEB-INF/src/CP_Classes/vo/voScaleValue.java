package CP_Classes.vo;

public class voScaleValue {

	private int iLowValue;
	private int iHighValue;
	private String sScaleDesc;
	
	public void setLowValue(int iLowValue) {
		this.iLowValue = iLowValue;
	}
	
	public int getLowValue() {
		return iLowValue;
	}

	public void setHighValue(int iHighValue) {
		this.iHighValue = iHighValue;
	}

	public int getHighValue() {
		return iHighValue;
	}

	public void setScaleDesc(String sScaleDesc) {
		this.sScaleDesc = sScaleDesc;
	}

	public String getScaleDesc() {
		return sScaleDesc;
	}
	
	
	
}
