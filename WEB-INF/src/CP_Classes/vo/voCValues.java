package CP_Classes.vo;

public class voCValues {

	private int	iNumRank	= 0 ;
	private int iRankPos	= 0 ;	
	private int	iCValue	= 0 ;
	
	public int getCValue() {
		return iCValue;
	}
	public void setCValue(int value) {
		iCValue = value;
	}
	public int getNumRank() {
		return iNumRank;
	}
	public void setNumRank(int numRank) {
		iNumRank = numRank;
	}
	public int getRankPos() {
		return iRankPos;
	}
	public void setRankPos(int rankPos) {
		iRankPos = rankPos;
	}	
	
}
